    def test_build_dynamic_codeuri_generates_mappings(self):
        """
        Test that sam build with dynamic CodeUri generates Mappings in the output template.

        This test verifies:
        1. Build succeeds with dynamic CodeUri template
        2. The built template contains a Mappings section with SAMCodeUriFunctions
        3. CodeUri is replaced with Fn::FindInMap referencing the Mappings
        4. Each collection value maps to its correct build artifact directory
        5. Fn::ForEach structure is preserved in the output

        Validates Requirements: 6.6, 16.6
        """
        # Use the dynamic CodeUri template
        self.template_path = str(Path(self.test_data_path, "language-extensions-dynamic-codeuri", "template.yaml"))

        runtime = self._get_python_version()
        overrides = {"Runtime": runtime}

        cmdlist = self.get_command_list(parameter_overrides=overrides)
        command_result = run_command(cmdlist, cwd=self.working_dir)

        self.assertEqual(
            command_result.process.returncode, 0, f"Build failed with stderr: {command_result.stderr.decode('utf-8')}"
        )

        # Read the built template
        built_template_path = self.default_build_dir.joinpath("template.yaml")
        self.assertTrue(built_template_path.exists(), "Built template.yaml should exist")

        with open(built_template_path, "r") as f:
            built_template = yaml.safe_load(f)

        # Verify Fn::ForEach structure is preserved
        resources = built_template.get("Resources", {})
        foreach_key = "Fn::ForEach::Functions"
        self.assertIn(foreach_key, resources, "Fn::ForEach::Functions should be preserved in built template")

        foreach_block = resources[foreach_key]
        self.assertIsInstance(foreach_block, list)
        self.assertEqual(len(foreach_block), 3)
        self.assertEqual(foreach_block[0], "FunctionName")
        self.assertEqual(foreach_block[1], ["Alpha", "Beta"])

        # Verify Mappings section was generated
        mappings = built_template.get("Mappings", {})
        mapping_name = "SAMCodeUriFunctions"
        self.assertIn(mapping_name, mappings, f"Built template should contain {mapping_name} Mapping")

        # Verify each collection value has an entry in the Mapping
        mapping_entries = mappings[mapping_name]
        self.assertIn("Alpha", mapping_entries, "Mapping should have entry for Alpha")
        self.assertIn("Beta", mapping_entries, "Mapping should have entry for Beta")

        # Verify each entry has a CodeUri pointing to the build artifact
        alpha_codeuri = mapping_entries["Alpha"].get("CodeUri")
        beta_codeuri = mapping_entries["Beta"].get("CodeUri")
        self.assertIsNotNone(alpha_codeuri, "Alpha should have a CodeUri in Mapping")
        self.assertIsNotNone(beta_codeuri, "Beta should have a CodeUri in Mapping")

        # The build artifact paths should be different (each function has its own source)
        self.assertNotEqual(alpha_codeuri, beta_codeuri, "Alpha and Beta should have different build artifact paths")

        # Verify CodeUri in the ForEach body is replaced with Fn::FindInMap
        body = foreach_block[2]
        function_template = body.get("${FunctionName}Function", {})
        properties = function_template.get("Properties", {})
        codeuri = properties.get("CodeUri")

        self.assertIsInstance(codeuri, dict, "CodeUri should be a dict (Fn::FindInMap)")
        self.assertIn("Fn::FindInMap", codeuri, "CodeUri should use Fn::FindInMap")

        findmap_args = codeuri["Fn::FindInMap"]
        self.assertEqual(findmap_args[0], mapping_name, f"FindInMap should reference {mapping_name}")
        self.assertEqual(findmap_args[1], {"Ref": "FunctionName"}, "FindInMap should use Ref to loop variable")
        self.assertEqual(findmap_args[2], "CodeUri", "FindInMap should look up CodeUri key")

        LOG.info("Successfully verified dynamic CodeUri generates Mappings in build output")

