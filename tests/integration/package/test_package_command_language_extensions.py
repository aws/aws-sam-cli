    def test_package_nested_foreach_dynamic_codeuri_generates_mappings(self):
        """
        Test that sam package with nested Fn::ForEach and dynamic CodeUri generates Mappings.

        Validates Requirements: 25.5, 25.9
        """
        template_path = self.test_data_path.joinpath(
            "language-extensions-nested-foreach-dynamic-codeuri", "template.yaml"
        )

        output_template = tempfile.NamedTemporaryFile(delete=False, suffix=".yaml")
        try:
            command_list = self.get_command_list(
                s3_bucket=self.s3_bucket.name,
                template_file=str(template_path),
                output_template_file=output_template.name,
            )

            process = Popen(command_list, stdout=PIPE, stderr=PIPE)
            try:
                stdout, stderr = process.communicate(timeout=TIMEOUT)
            except TimeoutExpired:
                process.kill()
                raise

            self.assertEqual(
                process.returncode,
                0,
                f"Package failed: {stderr.decode('utf-8')}",
            )

            with open(output_template.name, "r") as f:
                packaged_template = yaml_parse(f.read())

            # Verify nested Fn::ForEach structure is preserved
            resources = packaged_template.get("Resources", {})
            outer_key = "Fn::ForEach::Environments"
            self.assertIn(outer_key, resources)

            outer_block = resources[outer_key]
            inner_block = outer_block[2]["Fn::ForEach::Services"]
            self.assertEqual(inner_block[0], "Service")
            self.assertEqual(inner_block[1], ["Users", "Orders"])

            # Verify Mappings section was generated
            mappings = packaged_template.get("Mappings", {})
            mapping_name = "SAMCodeUriEnvironmentsServices"
            self.assertIn(mapping_name, mappings)
            self.assertIn("Users", mappings[mapping_name])
            self.assertIn("Orders", mappings[mapping_name])

            # Verify S3 URIs in Mappings
            users_uri = mappings[mapping_name]["Users"]["CodeUri"]
            orders_uri = mappings[mapping_name]["Orders"]["CodeUri"]
            self.assertTrue(users_uri.startswith("s3://"), f"Users URI should be S3: {users_uri}")
            self.assertTrue(orders_uri.startswith("s3://"), f"Orders URI should be S3: {orders_uri}")

            # Verify CodeUri replaced with Fn::FindInMap
            inner_body = inner_block[2]
            props = inner_body["${Env}${Service}Function"]["Properties"]
            codeuri = props["CodeUri"]
            self.assertIsInstance(codeuri, dict)
            self.assertIn("Fn::FindInMap", codeuri)
            self.assertEqual(codeuri["Fn::FindInMap"][0], mapping_name)
        finally:
            os.unlink(output_template.name)
