    def test_package_emits_warning_for_param_based_collection_with_dynamic_codeuri(self):
        """
        Test that sam package emits warning for parameter-based collection with dynamic CodeUri.

        This test verifies:
        1. Package succeeds with parameter-based collection and dynamic CodeUri
        2. A warning is emitted about collection values being fixed at package time
        3. Packaged template contains generated Mappings section
        4. Fn::ForEach structure is preserved

        Validates Requirements: 4.8
        """
        template_path = self.test_data_path.joinpath("language-extensions-param-dynamic-codeuri", "template.yaml")

        with tempfile.NamedTemporaryFile(delete=False, suffix=".yaml") as output_template:
            command_list = PackageIntegBase.get_command_list(
                s3_bucket=self.s3_bucket.name,
                template=template_path,
                s3_prefix=self.s3_prefix,
                output_template_file=output_template.name,
                force_upload=True,
            )

            process = Popen(command_list, stdout=PIPE, stderr=PIPE)
            try:
                stdout, stderr = process.communicate(timeout=TIMEOUT)
            except TimeoutExpired:
                process.kill()
                raise

            process_stdout = stdout.strip().decode("utf-8")
            process_stderr = stderr.strip().decode("utf-8")

            # Verify package succeeds
            self.assertEqual(
                process.returncode,
                0,
                f"Package failed with stdout: {process_stdout}, stderr: {process_stderr}",
            )

            # Requirement 4.8: Verify warning is emitted about collection values being fixed at package time
            # The warning should mention:
            # - The Fn::ForEach loop name (Services)
            # - Dynamic CodeUri
            # - Parameter-based collection
            # - Need to re-package if parameter changes
            combined_output = process_stdout + process_stderr

            # Check for warning about parameter-based collection with dynamic CodeUri
            warning_patterns = [
                r"[Ww]arning",  # Should contain "Warning" or "warning"
                r"Services",  # Should mention the ForEach loop name
                r"(parameter|re-?package|fixed|package time)",  # Should mention parameter or re-package
            ]

            warning_found = False
            for pattern in warning_patterns:
                if re.search(pattern, combined_output, re.IGNORECASE):
                    warning_found = True
                    break

            self.assertTrue(
                warning_found,
                f"Expected warning about parameter-based collection with dynamic CodeUri. "
                f"Output was:\nstdout: {process_stdout}\nstderr: {process_stderr}",
            )

            # Verify success message is still present
            self.assertIn(
                f"Successfully packaged artifacts and wrote output template to file {output_template.name}",
                process_stdout,
            )

            # Read the packaged template using SAM CLI's YAML parser
            with open(output_template.name, "r") as f:
                packaged_template = yaml_parse(f.read())

            # Verify Mappings section is generated with correct naming
            mappings = packaged_template.get("Mappings", {})
            mapping_name = "SAMCodeUriServices"  # SAM{PropertyName}{LoopName}

            self.assertIn(
                mapping_name,
                mappings,
                f"Packaged template should contain generated Mappings section '{mapping_name}'",
            )

            # Verify Mappings contains entries for each default collection value
            mapping_entries = mappings[mapping_name]
            self.assertIn("Users", mapping_entries, "Mappings should contain entry for 'Users'")
            self.assertIn("Orders", mapping_entries, "Mappings should contain entry for 'Orders'")

            # Verify each artifact has S3 URI
            users_uri = mapping_entries["Users"].get("CodeUri")
