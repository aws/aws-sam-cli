"""
Integration tests for sam package with CloudFormation Language Extensions.

These tests verify that sam package correctly processes templates using
AWS::LanguageExtensions transform, including Fn::ForEach with static and dynamic CodeUri.

Requirements tested:
- 4.2: sam package uploads each artifact to S3 with unique content-based hash key
- 4.3: sam package generates Mappings section for dynamic artifact properties
- 4.4: sam package replaces dynamic artifact property with Fn::FindInMap
- 4.5: sam package preserves Fn::ForEach structure (not expand to individual resources)
- 4.6: Generated Mappings section name follows pattern SAM{PropertyName}{LoopName}
- 4.8: sam package emits warning for parameter-based collection with dynamic CodeUri
- 13.1: sam package uploads shared artifact once for Fn::ForEach with static CodeUri
- 13.2: sam package preserves Fn::ForEach structure in packaged template
- 13.3: S3 URI is the same for all functions generated by Fn::ForEach with static CodeUri
- 16.7: Integration tests verify sam package preserves Fn::ForEach structure
"""

import json
import os
import re
import tempfile
from pathlib import Path
from subprocess import Popen, PIPE, TimeoutExpired
from unittest import skipIf

from samcli.yamlhelper import yaml_parse

from .package_integ_base import PackageIntegBase
from tests.testing_utils import RUNNING_ON_CI, RUNNING_TEST_FOR_MASTER_ON_CI, RUN_BY_CANARY

# Package tests require credentials and CI/CD will only add credentials to the env if the PR is from the same repo.
# This is to restrict package tests to run outside of CI/CD, when the branch is not master and tests are not run by Canary.
SKIP_PACKAGE_TESTS = RUNNING_ON_CI and RUNNING_TEST_FOR_MASTER_ON_CI and not RUN_BY_CANARY
TIMEOUT = 300


@skipIf(SKIP_PACKAGE_TESTS, "Skip package tests in CI/CD only")
class TestPackageLanguageExtensions(PackageIntegBase):
    """
    Integration tests for sam package with CloudFormation Language Extensions.

    Tests that sam package correctly processes templates with:
    - Fn::ForEach generating multiple Lambda functions with static CodeUri
    - Fn::ForEach generating multiple Lambda functions with dynamic CodeUri
    - Preservation of Fn::ForEach structure in packaged template
    - Same S3 URI for all functions with static CodeUri
    - Generated Mappings section for dynamic CodeUri
    - Fn::FindInMap replacement for dynamic artifact properties
    - Unique content-based S3 keys for each artifact

    Validates Requirements: 4.2, 4.3, 4.4, 4.5, 4.6, 4.8, 13.1, 13.2, 13.3, 16.7
    """

    @classmethod
    def setUpClass(cls):
        super().setUpClass()
        # Override test_data_path to point to the language extensions test data
        cls.test_data_path = Path(__file__).resolve().parents[1].joinpath("testdata", "package")

    def test_package_preserves_foreach_structure_with_static_codeuri(self):
        """
        Test that sam package preserves Fn::ForEach structure with static CodeUri.

        This test verifies:
        1. Package succeeds with language extensions template
        2. Packaged template preserves the Fn::ForEach structure
        3. S3 URI is the same for all functions generated by Fn::ForEach
        4. The shared artifact is uploaded once

        Validates Requirements: 13.1, 13.2, 13.3, 16.7
        """
        template_path = self.test_data_path.joinpath("language-extensions-foreach", "template.yaml")

        with tempfile.NamedTemporaryFile(delete=False, suffix=".yaml") as output_template:
            command_list = PackageIntegBase.get_command_list(
                s3_bucket=self.s3_bucket.name,
                template=template_path,
                s3_prefix=self.s3_prefix,
                output_template_file=output_template.name,
                force_upload=True,
            )

            process = Popen(command_list, stdout=PIPE, stderr=PIPE)
            try:
                stdout, stderr = process.communicate(timeout=TIMEOUT)
            except TimeoutExpired:
                process.kill()
                raise

            process_stdout = stdout.strip().decode("utf-8")
            process_stderr = stderr.strip().decode("utf-8")

            # Verify package succeeds
            self.assertEqual(
                process.returncode,
                0,
                f"Package failed with stdout: {process_stdout}, stderr: {process_stderr}",
            )

            # Verify success message
            self.assertIn(
                f"Successfully packaged artifacts and wrote output template to file {output_template.name}",
                process_stdout,
            )

            # Read the packaged template using SAM CLI's YAML parser
            with open(output_template.name, "r") as f:
                packaged_template = yaml_parse(f.read())

            # Requirement 13.2, 16.7: Verify Fn::ForEach structure is preserved
            resources = packaged_template.get("Resources", {})
            foreach_key = "Fn::ForEach::Functions"

            self.assertIn(
                foreach_key,
                resources,
                f"Packaged template should preserve {foreach_key} structure",
            )

            # Verify the Fn::ForEach structure is correct
            foreach_block = resources[foreach_key]
            self.assertIsInstance(foreach_block, list, "Fn::ForEach block should be a list")
            self.assertEqual(
                len(foreach_block),
                3,
                "Fn::ForEach block should have 3 elements: variable, collection, body",
            )

            # Verify the loop variable
            loop_variable = foreach_block[0]
            self.assertEqual(loop_variable, "FunctionName", "Loop variable should be 'FunctionName'")

            # Verify the collection
            collection = foreach_block[1]
            self.assertIsInstance(collection, list, "Collection should be a list")
            self.assertEqual(collection, ["Alpha", "Beta"], "Collection should contain ['Alpha', 'Beta']")

            # Verify the body template contains the function definition
            body_template = foreach_block[2]
            self.assertIsInstance(body_template, dict, "Body template should be a dict")
            self.assertIn("${FunctionName}Function", body_template, "Body should contain function template")

            # Verify the function template structure
            function_template = body_template["${FunctionName}Function"]
            self.assertEqual(
                function_template.get("Type"),
                "AWS::Serverless::Function",
                "Function template should have correct Type",
            )

            # Requirement 13.3: Verify CodeUri is updated to S3 URI
            properties = function_template.get("Properties", {})
            code_uri = properties.get("CodeUri")

            self.assertIsNotNone(code_uri, "CodeUri should be present in function template")
            self.assertIsInstance(code_uri, str, "CodeUri should be a string (S3 URI)")
            self.assertTrue(
                code_uri.startswith(f"s3://{self.s3_bucket.name}/"),
                f"CodeUri should be an S3 URI starting with s3://{self.s3_bucket.name}/, got: {code_uri}",
            )

            # Requirement 13.1: Verify the artifact was uploaded (check stderr for upload message)
            # Since all functions share the same static CodeUri, there should be only one upload
            uploads = re.findall(r"Uploading to.+", process_stderr)
            self.assertGreaterEqual(
                len(uploads),
                1,
                "At least one artifact should be uploaded",
            )

            # Clean up
            os.unlink(output_template.name)

